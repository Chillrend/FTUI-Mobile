package com.rsypj.ftuimobile.model;

public class NoTelphoneModel {

    String name;
    String noPhone;

    public NoTelphoneModel(String name, String noPhone) {
        this.name = name;
        this.noPhone = noPhone;
    }

    public String getName() {
        return name;
    }

    public String getNoPhone() {
        return noPhone;
    }
}
